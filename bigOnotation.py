def print_first_item(items):
    print(items[0])

def print_all_items(items):
    for item in items:
        print(item)

def print_all_possible_ordered_pairs(items):
    for first_item in items:
        print(items)
        for second_item in items:
            print(second_item)
            print (first_item, second_item)
items = [1,2,3,4]
